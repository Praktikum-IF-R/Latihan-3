package overloading;
public class tugasLuas {
    private double prsgpjg;
    private double trpsm;
    public tugasLuas(){
        prsgpjg = 0;
        trpsm = 0;
    }
    public double getLuas(int p, int l){
        return prsgpjg = p*l; 
    }
    public double getLuas (int a, int b, int t){
        return trpsm = ((a+b)*t)/2;
    }
}
