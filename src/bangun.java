public class bangun {
    private double PP;
    private double Trp;
    public bangun(){
        PP = 0;
        Trp = 0;
    }
    public double getLuas(double p, double l){
        return PP = p*l;
    }
    public double getLuas(double a, double b, double t){
        return Trp = ((a+b)*t)/2;
    }
}
